// Organização básica do Javascript.

// O código JS pode ser organizado de duas maneiras:

// 1. Por "Sentensas de Código", que são, própriamente falando, linhas de código. Ex.: 

console.log("Sentença de Código")

// P.s.: As sentenças podem acabar ou não com ";"

// 2. Por "Blocos de Código", que são as Sentenças de Código agrupadas. Ex.: 

{

}

// 2.1 Um bloco de código sempre conterá um delimitador, o mais comum são as chaves acima. E, sim, exato,
// existem blocos de códigos vazios (chaves vazias). Adicionando algumas sentenças no bloco...

{
    {
        console.log('Olá, ')
        console.log('Mundo!')
    }
}

// 2.2 Existem vários tipos de blocos...